    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
      SubjectDBContext sdb = new SubjectDBContext();
        ArrayList<Subject> subject = sdb.list();
        for (Subject s : subject) {
             request.getSession().setAttribute("subject", sdb.get(s));
             break;
        }
        LectureDBContext ldb = new LectureDBContext();
        ArrayList<Lecture> lecture = ldb.list();
        for (Lecture l : lecture) {
             request.getSession().setAttribute("lecturer", ldb.get(l));
           
        }
        GroupDBContext gdb = new GroupDBContext();
        request.setAttribute("group", gdb.list());
       request.getRequestDispatcher("/View/Attendance/attendance.jsp").forward(request, response);
    } 

